function [s11,s12,s13,s22,s23,s33]=computeStressVerticalShearZone( ...
    x1,x2,x3,q1,q2,q3,L,T,W,theta,epsv11p,epsv12p,epsv13p,epsv22p,epsv23p,epsv33p,G,nu)
% function COMPUTESTRESSVERTICALSHEARZONE computes the stress field 
% associated with deforming vertical shear zones using the analytic 
% solution considering the following geometry.
%
%                      N (x1)
%                     /
%                    /| strike (theta)          E (x2)
%        q1,q2,q3 ->@--------------------------+
%                   |                        w |     +
%                   |                        i |    /
%                   |                        d |   / s
%                   |                        t |  / s
%                   |                        h | / e
%                   |                          |/ n
%                   +--------------------------+  k
%                   :       l e n g t h       /  c
%                   |                        /  i
%                   :                       /  h
%                   |                      /  t
%                   :                     /
%                   |                    +
%                   Z (x3)
%
%
% Input:
% x1, x2, x3         northing, easting, and depth of the observation point,
% q1, q2, q3         north, east and depth coordinates of the shear zone,
% L, T, W            length, thickness, and width of the shear zone,
% theta (degree)     strike of the shear zone,
% epsvijp            source strain component 11, 12, 13, 22, 23 and 33 
%                    in the shear zone in the system of reference tied to 
%                    the shear zone (primed reference system),
% G, nu              shear modulus and Poisson's ratio in the half space.
%
% Output:
% sij                stress components in the unprimed reference system.
%
% Author: 
% Sylvain Barbot and James D. P. Moore (earth@jamesdpmoore.com) - 11 Jul, 2016,
% Earth Observatory of Singapore
%
% SEE ALSO: unicycle

assert(-1<=nu & 1/2>=nu,'unicycle:greens:computeStressVerticalShearZone -1<nu<0.5');
assert(0<=min(x3),'unicycle:greens:computeStressVerticalShearZone depth must be positive');
assert(0<=q3,'unicycle:greens:computeStressVerticalShearZone depth must be positive');

% Lame parameter
lambda=G*2*nu/(1-2*nu);

% isotropic strain
epsvkk=epsv11p+epsv22p+epsv33p;

% rotate observation points to the shear-zone-centric system of coordinates
t1= (x1-q1)*cosd(theta)+(x2-q2)*sind(theta);
x2=-(x1-q1)*sind(theta)+(x2-q2)*cosd(theta);
x1=t1;

% Green's functions
r1=@(y1,y2,y3) sqrt((x1-y1).^2+(x2-y2).^2+(x3-y3).^2);
r2=@(y1,y2,y3) sqrt((x1-y1).^2+(x2-y2).^2+(x3+y3).^2);

% displacement gradient
e11p= IU1d1(L,T/2,q3+W)-IU1d1(L,-T/2,q3+W)+IU1d1(L,-T/2,q3)-IU1d1(L,T/2,q3) ...
     -IU1d1(0,T/2,q3+W)+IU1d1(0,-T/2,q3+W)-IU1d1(0,-T/2,q3)+IU1d1(0,T/2,q3);
u12 = IU1d2(L,T/2,q3+W)-IU1d2(L,-T/2,q3+W)+IU1d2(L,-T/2,q3)-IU1d2(L,T/2,q3) ...
     -IU1d2(0,T/2,q3+W)+IU1d2(0,-T/2,q3+W)-IU1d2(0,-T/2,q3)+IU1d2(0,T/2,q3);
u13 = IU1d3(L,T/2,q3+W)-IU1d3(L,-T/2,q3+W)+IU1d3(L,-T/2,q3)-IU1d3(L,T/2,q3) ...
     -IU1d3(0,T/2,q3+W)+IU1d3(0,-T/2,q3+W)-IU1d3(0,-T/2,q3)+IU1d3(0,T/2,q3);
u21 = IU2d1(L,T/2,q3+W)-IU2d1(L,-T/2,q3+W)+IU2d1(L,-T/2,q3)-IU2d1(L,T/2,q3) ...
     -IU2d1(0,T/2,q3+W)+IU2d1(0,-T/2,q3+W)-IU2d1(0,-T/2,q3)+IU2d1(0,T/2,q3);
e22p= IU2d2(L,T/2,q3+W)-IU2d2(L,-T/2,q3+W)+IU2d2(L,-T/2,q3)-IU2d2(L,T/2,q3) ...
     -IU2d2(0,T/2,q3+W)+IU2d2(0,-T/2,q3+W)-IU2d2(0,-T/2,q3)+IU2d2(0,T/2,q3);
u23 = IU2d3(L,T/2,q3+W)-IU2d3(L,-T/2,q3+W)+IU2d3(L,-T/2,q3)-IU2d3(L,T/2,q3) ...
     -IU2d3(0,T/2,q3+W)+IU2d3(0,-T/2,q3+W)-IU2d3(0,-T/2,q3)+IU2d3(0,T/2,q3);
u31 = IU3d1(L,T/2,q3+W)-IU3d1(L,-T/2,q3+W)+IU3d1(L,-T/2,q3)-IU3d1(L,T/2,q3) ...
     -IU3d1(0,T/2,q3+W)+IU3d1(0,-T/2,q3+W)-IU3d1(0,-T/2,q3)+IU3d1(0,T/2,q3);
u32 = IU3d2(L,T/2,q3+W)-IU3d2(L,-T/2,q3+W)+IU3d2(L,-T/2,q3)-IU3d2(L,T/2,q3) ...
     -IU3d2(0,T/2,q3+W)+IU3d2(0,-T/2,q3+W)-IU3d2(0,-T/2,q3)+IU3d2(0,T/2,q3);
e33 = IU3d3(L,T/2,q3+W)-IU3d3(L,-T/2,q3+W)+IU3d3(L,-T/2,q3)-IU3d3(L,T/2,q3) ...
     -IU3d3(0,T/2,q3+W)+IU3d3(0,-T/2,q3+W)-IU3d3(0,-T/2,q3)+IU3d3(0,T/2,q3);

% strain
e12p=(u12+u21)/2;
e13p=(u13+u31)/2;
e23p=(u23+u32)/2;

% rotate stress field to reference (unprimed) system of coordinates
e11=(cosd(theta)*e11p-sind(theta)*e12p)*cosd(theta)-(cosd(theta)*e12p-sind(theta)*e22p)*sind(theta);
e12=(cosd(theta)*e11p-sind(theta)*e12p)*sind(theta)+(cosd(theta)*e12p-sind(theta)*e22p)*cosd(theta);
e13= cosd(theta)*e13p-sind(theta)*e23p;
e22=(sind(theta)*e11p+cosd(theta)*e12p)*sind(theta)+(sind(theta)*e12p+cosd(theta)*e22p)*cosd(theta);
e23= sind(theta)*e13p+cosd(theta)*e23p;

% remove inelastic eigenstrain
Omega=@(x) heaviside(x+0.5)-heaviside(x-0.5);
S=@(x) Omega(x-0.5);

epsv11=(cosd(theta)*epsv11p-sind(theta)*epsv12p)*cosd(theta)-(cosd(theta)*epsv12p-sind(theta)*epsv22p)*sind(theta);
epsv12=(cosd(theta)*epsv11p-sind(theta)*epsv12p)*sind(theta)+(cosd(theta)*epsv12p-sind(theta)*epsv22p)*cosd(theta);
epsv13= cosd(theta)*epsv13p-sind(theta)*epsv23p;
epsv22=(sind(theta)*epsv11p+cosd(theta)*epsv12p)*sind(theta)+(sind(theta)*epsv12p+cosd(theta)*epsv22p)*cosd(theta);
epsv23= sind(theta)*epsv13p+cosd(theta)*epsv23p;
epsv33=epsv33p;

e11=e11-epsv11*S(x1/L).*Omega(x2/T).*S((x3-q3)/W);
e12=e12-epsv12*S(x1/L).*Omega(x2/T).*S((x3-q3)/W);
e13=e13-epsv13*S(x1/L).*Omega(x2/T).*S((x3-q3)/W);
e22=e22-epsv22*S(x1/L).*Omega(x2/T).*S((x3-q3)/W);
e23=e23-epsv23*S(x1/L).*Omega(x2/T).*S((x3-q3)/W);
e33=e33-epsv33*S(x1/L).*Omega(x2/T).*S((x3-q3)/W);

% stress components
s11=lambda*(e11+e22+e33)+2*G*e11;
s12=2*G*e12;
s13=2*G*e13;
s22=lambda*(e11+e22+e33)+2*G*e22;
s23=2*G*e23;
s33=lambda*(e11+e22+e33)+2*G*e33;

    function y=heaviside(x)
        y=x>0;
    end

    function y=xLogy(x,y)
        y=x.*log(y);
        y(0==x)=0;
    end

    function u = IU1d1(y1,y2,y3)
        % function IU1d1 is the integrand for displacement gradient u1,1
        u=zeros(size(x1));
        
        if epsv11p ~= 0 || epsvkk ~= 0
            u=u+(lambda*epsvkk+2*G*epsv11p)*J1123d1(y1,y2,y3);
        end
        if epsv12p ~= 0
            u=u+2*G*epsv12p*(J1223d1(y1,y2,y3)+J1113d1(y1,y2,y3));
        end
        if epsv13p ~= 0
            u=u+2*G*epsv13p*(J1323d1(y1,y2,y3)+J1112d1(y1,y2,y3));
        end
        if epsv22p ~= 0 || epsvkk ~= 0
            u=u+(lambda*epsvkk+2*G*epsv22p)*J1213d1(y1,y2,y3);
        end
        if epsv23p ~= 0
            u=u+2*G*epsv23p*(J1212d1(y1,y2,y3)+J1313d1(y1,y2,y3));
        end
        if epsv33p ~= 0 || epsvkk ~= 0
            u=u+(lambda*epsvkk+2*G*epsv33p)*J1312d1(y1,y2,y3);
        end
    end

    function u = IU1d2(y1,y2,y3)
        % function IU1d2 is the integrand for displacement gradient u1,2
        u=zeros(size(x1));
        
        if epsv11p ~= 0 || epsvkk ~= 0
            u=u+(lambda*epsvkk+2*G*epsv11p)*J1123d2(y1,y2,y3);
        end
        if epsv12p ~= 0
            u=u+2*G*epsv12p*(J1223d2(y1,y2,y3)+J1113d2(y1,y2,y3));
        end
        if epsv13p ~= 0
            u=u+2*G*epsv13p*(J1323d2(y1,y2,y3)+J1112d2(y1,y2,y3));
        end
        if epsv22p ~= 0 || epsvkk ~= 0
            u=u+(lambda*epsvkk+2*G*epsv22p)*J1213d2(y1,y2,y3);
        end
        if epsv23p ~= 0
            u=u+2*G*epsv23p*(J1212d2(y1,y2,y3)+J1313d2(y1,y2,y3));
        end
        if epsv33p ~= 0 || epsvkk ~= 0
            u=u+(lambda*epsvkk+2*G*epsv33p)*J1312d2(y1,y2,y3);
        end
    end

    function u = IU1d3(y1,y2,y3)
        % function IU1d3 is the integrand for displacement gradient u1,3
        u=zeros(size(x1));
        
        if epsv11p ~= 0 || epsvkk ~= 0
            u=u+(lambda*epsvkk+2*G*epsv11p)*J1123d3(y1,y2,y3);
        end
        if epsv12p ~= 0
            u=u+2*G*epsv12p*(J1223d3(y1,y2,y3)+J1113d3(y1,y2,y3));
        end
        if epsv13p ~= 0
            u=u+2*G*epsv13p*(J1323d3(y1,y2,y3)+J1112d3(y1,y2,y3));
        end
        if epsv22p ~= 0 || epsvkk ~= 0
            u=u+(lambda*epsvkk+2*G*epsv22p)*J1213d3(y1,y2,y3);
        end
        if epsv23p ~= 0
            u=u+2*G*epsv23p*(J1212d3(y1,y2,y3)+J1313d3(y1,y2,y3));
        end
        if epsv33p ~= 0 || epsvkk ~= 0
            u=u+(lambda*epsvkk+2*G*epsv33p)*J1312d3(y1,y2,y3);
        end
    end

    function u = IU2d1(y1,y2,y3)
        % function IU2d1 is the integrand for displacement gradient u2,1
        u=zeros(size(x1));
        
        if epsv11p ~= 0 || epsvkk ~= 0
            u=u+(lambda*epsvkk+2*G*epsv11p)*J2123d1(y1,y2,y3);
        end
        if epsv12p ~= 0
            u=u+2*G*epsv12p*(J2223d1(y1,y2,y3)+J2113d1(y1,y2,y3));
        end
        if epsv13p ~= 0
            u=u+2*G*epsv13p*(J2323d1(y1,y2,y3)+J2112d1(y1,y2,y3));
